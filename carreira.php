    <div class="text">
        Marina Lambrini Diamandis tem as raízes no País de Gales e ascendência grega por parte de pai.
        Foi na adolescência que ela descobriu que queria se tornar musicista.
        Seu professor de música na Haberdashers’ Monmouth School for Girls a convenceu que ela poderia fazer o que quisesse, e ela levou isso a sério.
        Aos 16 anos ela foi morar na Grécia com o pai para se conectar com suas origens, e quando retornou, aos 18, trabalhou num posto de gasolina para ganhar dinheiro e continuar estudando música. 
        Seu aprendizado formal nas universidades londrinas que frequentou não foi adiante, no entanto, Marina estava cada vez mais obcecada pelo objetivo de se tornar uma cantora.
        Ela tentou seguir os mesmos passos das Spice Girls e se apresentou em rádios atrás da oportunidade de ser descoberta e participou de inúmeras outras audições nesse meio tempo
        Em 2007, finalmente, ela decidiu produzir seu primeiro extended play, “Mermaid vs. Sailors”, de forma independente e o compartilhou no MySpace. 
        Marina tentou negociações com mais de dez gravadoras, até decidir fechar com a Neon Gold Records, por acreditar que esta não tentaria ditar sua imagem.
        Dois anos depois, ela lançou seu single de estreia “Obsessions”, seguido do segundo EP, “The Crown Jewels”, que seria o predecessor e introdutor do seu primeiro álbum completo de estúdio.
        Até esse ponto, percebemos que a carreira de Marina foi premeditada por ela mesma, seu caminho foi construído com esforço e independe de ter crescido em um ambiente musical.
        Ela pertence àquele grupo de artistas com começos árduos que viram na música sua vocação e o meio de deixar sua marca.
